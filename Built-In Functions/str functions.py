# 1. str.lower()
my_string = "Hello, World!"
print("Lowercase:", my_string.lower())

# 2. str.upper()
print("Uppercase:", my_string.upper())

# 3. str.capitalize()
print("Capitalized:", my_string.capitalize())

# 4. str.title()
print("Titlecased:", my_string.title())

# 5. str.strip()
my_string = "   Hello, World!   "
print("Stripped:", my_string.strip())

# 6. str.split()
my_string = "Hello, World!"
print("Split:", my_string.split(','))

# 7. str.join()
my_list = ['Hello', 'World']
print("Joined:", ' '.join(my_list))

# 8. str.index()
print("Index of 'World':", my_string.index('World'))

# 9. str.isalnum()
print("Is alphanumeric:", my_string.isalnum())

# 10. str.isalpha()
print("Is alphabetic:", my_string.isalpha())

# 11. str.isdigit()
print("Is digit:", my_string.isdigit())

# 12. str.islower()
print("Is lowercase:", my_string.islower())

# 13. str.isupper()
print("Is uppercase:", my_string.isupper())

# 14. str.isnumeric()
print("Is numeric:", my_string.isnumeric())

# 15. str.isdecimal()
print("Is decimal:", my_string.isdecimal())
